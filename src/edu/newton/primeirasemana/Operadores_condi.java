package edu.newton.primeirasemana;

public class Operadores_condi {
    
    public static void main(String[] args) {
        
        int a, b;
    
        a = 2;
        b = 5;
    
        if (a == b){
            System.out.println("a é igual a b");
        } else if (a < b) {
            System.out.println("a é menor que b");
        } else if(a > b) {
            System.out.println("a é maior que b");
        } 
    }
}
